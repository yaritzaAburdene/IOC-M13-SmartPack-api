package com.smartpack.models;

/**
 * Enum que representa els diferents estats d'un paquet.
 * 
 * @see ORDENAT
 * @see ENVIAT
 * @see TRANSIT
 * @see ENTREGAT
 * @see NO_ENTREGAT
 * @see RETORNAT
 */
public enum Estat {
    /**
     * Estat ORDENAT
     */
    ORDENAT,
    /**
     * Estat EVIAT
     */
    ENVIAT,
    /**
     * Estat TRANSIT
     */
    TRANSIT,
    /**
     * Estat ENTREGAT
     */
    ENTREGAT,
    /**
     * Estat ENTREGAT
     */
    NO_ENTREGAT,
    /**
     * Estat ENTREGAT
     */
    RETORNAT
}
